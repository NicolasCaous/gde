	2) Buscar as disciplinas que j&aacute; cursou, acessar a aba Disciplina e clicar no link "Eliminar" localizada na aba Curso<br /><br />

	<strong>P: Como coloco uma disciplina de volta no meu curr&iacute;culo / &aacute;rvore?</strong><br />
	R: Para colocar a disciplina de volta na &aacute;rvore, efetue uma busca, clique na disciplina em quest&atilde;o, e clique no link "Nenhuma das Anteriores"...<br /><br />

