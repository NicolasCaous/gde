	<h3>Utilizando o GDE</h3><br />
	<strong>P: Como editar meu perfil?</strong><br />
	R: Para editar seu perfil, existe um link embaixo da sua foto (<i>Editar  Perfil</i>). L&aacute; voc&ecirc; pode por suas informa&ccedil;&otilde;es pessoais,
	foto, email para contato e por a&iacute; vai. Al&eacute;m de poder editar op&ccedil;&otilde;es de privacidade para sua &aacute;rvore de disciplinas.
	Aconselhamos que deixe seu perfil sempre atualizado.<br /><br />

	<strong>P: Como adiciono um amigo?</strong><br />
	R: Primeiramente, deve-se fazer uma busca pelo nome de seu amigo utilizando o campo no canto direito superior.
	Ao encontrar seu amigo, selecione-o e clique no link para adicion&aacute;-lo. <br /><br />

	<strong>P: Como aceitar um pedido de amizade?</strong><br />
	R: Os pedidos de amizade ficam perto do link <i><u>Amigos</u></i>, sendo uma imagem amarela. N&atilde;o h&aacute; como errar :) <br /><br />

	<strong>P: Como achar uma sala, um professor ou disciplina?</strong><br />
	R: Basta fazer uma pesquisa utilizando o campo de busca no canto direito superior e apertar o bot&atilde;o <i>Enter</i> ou clicar na lupa.
	Em seguida, os resultados aparecer&atilde;o. Entre na aba desejada e divirta-se. <br /><br />

	<strong>P: Como elimino do meu curr&iacute;culo / &aacute;rvore uma disciplina j&aacute; cursada?</strong><br />
